Print the even numbers from 1 to 99, inclusive. All numbers should be printed 
on separate lines.

Understand the Problem
-------------
Use puts to make sure we print on separate lines
Will need to iterate through some DS that contains the numbers between 1 through 99

Examples/Test Cases
-------------
print
2
4
6
8
...
98

Data Structures
-------------

options
  generate the list of numbers by using a for in loop
  while loop initializing a counter
  


Algorithm
-------------
SET counter = 2
WHILE the counter is < 99
  print the counter
  increment the counter by 2

